let agendamentos = JSON.parse(localStorage.getItem("agendamentos")) || [];

function carregarJogos() {
    const consoleSelecionado = document.getElementById("console").value;
    const selectJogo = document.getElementById("jogo");

    selectJogo.innerHTML = '<option value="">Escolha o Jogo</option>';

    if (jogosPorConsole[consoleSelecionado]) {
        jogosPorConsole[consoleSelecionado].forEach(jogo => {
            const option = document.createElement("option");
            option.value = jogo;
            option.textContent = jogo;
            selectJogo.appendChild(option);
        });
    }
}

function salvar() {
    localStorage.setItem("agendamentos", JSON.stringify(agendamentos));
}

function renderizar() {
    const lista = document.getElementById("lista-agendamentos");
    lista.innerHTML = "";

    agendamentos.forEach((item, index) => {
        const li = document.createElement("li");

        li.innerHTML = `
            <div>
                🎮 ${item.console.toUpperCase()} - ${item.jogo}<br>
                📅 ${item.data} às ${item.hora}
            </div>
            <button class="remover" onclick="remover(${index})">Excluir</button>
        `;

        lista.appendChild(li);
    });
}

function adicionarAgendamento() {
    const consoleSelecionado = document.getElementById("console").value;
    const jogo = document.getElementById("jogo").value;
    const data = document.getElementById("data").value;
    const hora = document.getElementById("hora").value;

    if (!consoleSelecionado || !jogo || !data || !hora) {
        alert("Preencha todos os campos!");
        return;
    }

    agendamentos.push({
        console: consoleSelecionado,
        jogo: jogo,
        data: data,
        hora: hora
    });

    // ordenar por data e hora
    agendamentos.sort((a, b) => {
        const dateA = new Date(a.data + "T" + a.hora);
        const dateB = new Date(b.data + "T" + b.hora);
        return dateA - dateB;
    });

    salvar();
    renderizar();

    // limpar formulário
    document.getElementById("console").value = "";
    document.getElementById("jogo").innerHTML = '<option value="">Escolha o Jogo</option>';
    document.getElementById("data").value = "";
    document.getElementById("hora").value = "";
}

function remover(index) {
    agendamentos.splice(index, 1);
    salvar();
    renderizar();
}

function agendar() {
    adicionarAgendamento();
}

window.addEventListener("load", () => {
    renderizar();
});